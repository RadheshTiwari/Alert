const App = () => {
  const [x, setx] = useState();
  const [y, sety] = useState();
  //var setz;
  const [z, setz] = useState();
  return (
    <View>
      <TextInput
        placeholder="value1"
        style={{fontSize: 30, borderWidth: 2, borderColor: 'black'}}
        onChangeText={input1 => setx(input1)}
        value={x}></TextInput>
      <TextInput
        placeholder="value2"
        style={{fontSize: 30, borderWidth: 2, borderColor: 'black'}}
        onChangeText={input2 => sety(input2)}
        value={y}></TextInput>
      <Button
        title="click"
        onPress={() => {
          let result = Number(x) + Number(y);

          if (result % 2 == 0) {
            Alert.alert('even');
          } else {
            Alert.alert('odd');
          }
          setz(result);
        }}
      />
      <Button
        title="RESET"
        onPress={() => {
          setz == 0;
          setx(0);
          sety(0);
        }}
      />
    </View>
  );
};
export default App;
